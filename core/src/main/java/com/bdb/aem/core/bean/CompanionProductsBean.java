package com.bdb.aem.core.bean;


/**
 * The Class CompanionProductsBean.
 */
public class CompanionProductsBean {
    
    /** The product label. */
    private String productTitle;
    
    /** The cta link. */
    private String ctaLink;
    
    /** The size. */
    private String size;
    
    /** The cat no. */
    private String catNo;
    
    /** The img atl text. */
    private String imgAtlText;
    
    /** The image. */
	private String image;
    
    /**
     * Gets the product label.
     *
     * @return the product label
     */
    public String getProductTitle() {
		return productTitle;
	}

	/**
	 * Gets the cta link.
	 *
	 * @return the cta link
	 */
	public String getCtaLink() {
		return ctaLink;
	}

	/**
	 * Gets the size.
	 *
	 * @return the size
	 */
	public String getSize() {
		return size;
	}

	/**
	 * Gets the cat no.
	 *
	 * @return the cat no
	 */
	public String getCatNo() {
		return catNo;
	}

	/**
	 * Gets the img atl text.
	 *
	 * @return the img atl text
	 */
	public String getImgAtlText() {
		return imgAtlText;
	}

	/**
	 * Gets the image.
	 *
	 * @return the image
	 */
	public String getImage() {
		return image;
	}
}
