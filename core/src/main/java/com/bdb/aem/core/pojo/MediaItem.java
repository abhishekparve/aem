package com.bdb.aem.core.pojo;

public class MediaItem {

	private String baseMaterialNumber;
	private String imageName;
	private String caption;
	private String mime;
	private String imageMetadata;
	private String imageType;

	public String getBaseMaterialNumber() {
		return baseMaterialNumber;
	}

	public void setBaseMaterialNumber(String baseMaterialNumber) {
		this.baseMaterialNumber = baseMaterialNumber;
	}

	public String getImageName() {
		return imageName;
	}

	public void setImageName(String imageName) {
		this.imageName = imageName;
	}

	public String getCaption() {
		return caption;
	}

	public void setCaption(String caption) {
		this.caption = caption;
	}

	public String getMime() {
		return mime;
	}

	public void setMime(String mime) {
		this.mime = mime;
	}

	public String getImageMetadata() {
		return imageMetadata;
	}

	public void setImageMetadata(String imageMetadata) {
		this.imageMetadata = imageMetadata;
	}

	public String getImageType() {
		return imageType;
	}

	public void setImageType(String imageType) {
		this.imageType = imageType;
	}

}
