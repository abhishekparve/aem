package com.bdb.aem.core.pojo;

/**
 * The Class PrefferedPdfInfo.
 */
public class PrefferedPdfInfo {

	/** The preferred pdf res path. */
	private String preferredPdfResPath;

	/** The is global. */
	private boolean isGlobal;

	/**
	 * Gets the preferred pdf res path.
	 *
	 * @return the preferred pdf res path
	 */
	public String getPreferredPdfResPath() {
		return preferredPdfResPath;
	}

	/**
	 * Sets the preferred pdf res path.
	 *
	 * @param preferredPdfResPath the new preferred pdf res path
	 */
	public void setPreferredPdfResPath(String preferredPdfResPath) {
		this.preferredPdfResPath = preferredPdfResPath;
	}

	/**
	 * Checks if is global.
	 *
	 * @return true, if is global
	 */
	public boolean isGlobal() {
		return isGlobal;
	}

	/**
	 * Sets the global.
	 *
	 * @param isGlobal the new global
	 */
	public void setGlobal(boolean isGlobal) {
		this.isGlobal = isGlobal;
	}
}
